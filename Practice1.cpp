#include <iostream>

using namespace std;

int main() {
	// 정수형 변수 선언
	int num1 = 10, num2 = 4;

	// 사칙연산 출력
	cout << "입력된 값: num1 = " << num1 << ", num2 = " << num2 << endl;
	cout << "덧셈 결과: " << num1 + num2 << endl;
	cout << "뺄셈 결과: " << num1 - num2 << endl;
	cout << "곱셈 결과: " << num1 * num2 << endl;

	// 나눗셈을 할 때 0으로 나누는 경우를 처리
	if (num2 != 0) {
		cout << "나눗셈 결과: " << num1 / num2 << endl;
	}
	else {
		cout << "나누기 연산에서 0으로 나눌 수 없습니다!" << endl;
	}

	return 0;
}

